package modul_1.latihan;

public class DataDiri {
    public static void main(String[] args) {
        // Deklarasi variabel
        String namaLengkap = "Pelangi Xena";
        String tempatLahir = "Lubuk Kapundung";
        String tanggalLahir = "14 September 2006";
        String golonganDarah = "O";
        int umur = 19;
        double tinggiBadan = 1.65;
        char jenisKelamin = 'P';
        String agama = "Islam";
        String pekerjaan = "Software Engineer";

        // Menampilkan data diri
        System.out.println("Nama Lengkap: " + namaLengkap);
        System.out.println("Tempat Lahir: " + tempatLahir);
        System.out.println("Tanggal Lahir: " + tanggalLahir);
        System.out.println("Golongan Darah: " + golonganDarah);
        System.out.println("Umur: " + umur);
        System.out.println("Tinggi Badan: " + tinggiBadan + " m");
        System.out.println("Jenis Kelamin: " + jenisKelamin);
        System.out.println("Agama: " + agama);
        System.out.println("Pekerjaan: " + pekerjaan);
    }
}