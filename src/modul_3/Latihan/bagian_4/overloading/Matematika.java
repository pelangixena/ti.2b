package modul_3.Latihan.bagian_4.overloading;

class Matematika {
    // Method tambah dengan 2 parameter bertipe int
    int tambah(int a, int b) {
        return a + b;
    }

    // Method tambah dengan 3 parameter bertipe data int
    int tambah(int a, int b, int c) {
        return a + b + c;
    }

    // Method tambah dengan 2 parameter bertipe data double
    double tambah(double a, double b) {
        return a + b;
    }
}
