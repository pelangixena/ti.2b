package modul_3.bagian_2;

class Mahasiswa {
    // Atribut private
    private String nama;
    private int umur;

    // Getter and Setter
    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public int getUmur() {
        return umur;
    }

    public void setUmur(int umur) {
        this.umur = umur;
    }
}
